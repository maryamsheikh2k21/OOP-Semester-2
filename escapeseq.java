//program involving all escape sequences
public class escapeseq
{
	public static void main(String args[])
	{
	//single quotes
	System.out.println("Sam\'s car");
	//double qoutes
	System.out.println("\"Hello World\"");
	//slash
	System.out.println("yes\\no");
	//carriage return
	System.out.println("twelve\r12");
	//line feed and carriage return
	System.out.println("line one\nline two");
	//tab space
	System.out.println("before tab\tafter tab");
	//one character back
	System.out.println("nine\b9");
	//feed/next page
	System.out.println("Hello World\f");
	}
}